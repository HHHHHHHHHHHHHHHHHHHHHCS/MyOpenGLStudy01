#include "_27_ShadowMapping_Base.h"
#include "CommonBaseScript.h"
#include "Camera.h"
#include "ImageHelper.h"

unsigned int _27_ShadowMapping_Base::planeVAO = 0;
unsigned int _27_ShadowMapping_Base::cubeVAO = 0;
unsigned int _27_ShadowMapping_Base::quadVAO = 0;

int _27_ShadowMapping_Base::DoMain()
{
	CommonBaseScript::InitOpenGL();
	GLFWwindow* window = CommonBaseScript::InitWindow();

	if (window == nullptr)
	{
		return -1;
	}

	BindPlaneVAO();
	BindCubeVAO();
	BindQuadVAO();


	//Frame
	//---------------------
	GLuint depthMapFBO;
	glGenFramebuffers(1, &depthMapFBO);

	const GLuint SHADOW_WIDTH = 1024, SHADOW_HEIGHT = 1024;
	GLuint depthMap;
	glGenTextures(1, &depthMap);
	glBindTexture(GL_TEXTURE_2D, depthMap);
	glTexImage2D(GL_TEXTURE_2D, 0, GL_DEPTH_COMPONENT, SHADOW_WIDTH, SHADOW_HEIGHT
	             , 0, GL_DEPTH_COMPONENT, GL_FLOAT, nullptr);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);

	glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
	glFramebufferTexture2D(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, GL_TEXTURE_2D, depthMap, 0);
	//glDrawBuffer glReadBuffer 告诉opengl不要画颜色
	//只要深度 提高效率
	glDrawBuffer(GL_NONE);
	glReadBuffer(GL_NONE);
	glBindFramebuffer(GL_FRAMEBUFFER, 0);


	//shader
	//------------------------
	Shader simpleDepthShader{"27_SimpleDepthShader"};
	Shader debugDepthQuadShader{"27_DebugDepthQuad"};
	Shader shadowBaseShader{"27_ShadowBase"};

	// load textures
	// -------------
	unsigned int woodTexture = ImageHelper::LoadTexture("wood.png");

	//lightSpaceMatrix
	//------------------------
	GLfloat near_plane = 1.0f, far_plane = 7.5f;
	glm::vec3 lightPos = glm::vec3(-2.0f, 4.0f, -1.0f);
	//left right bottom top near far
	glm::mat4 lightProjection = glm::ortho(-10.0f, 10.0f, -10.0f, 10.0f, near_plane, far_plane);
	//从eye看向center,up用于向上的向量cross用
	glm::mat4 lightView = glm::lookAt(lightPos
	                                  , glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, 1.0f, 0.0f));
	//把投影矩阵放到light位置
	glm::mat4 lightSpaceMatrix = lightProjection * lightView;

	//camera
	//------------------------
	Camera camera = Camera();
	camera.AddMouseEvent(window);
	CommonBaseScript::RegisterKeyEvent(window);


	glEnable(GL_DEPTH_TEST);

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, woodTexture);
	glActiveTexture(GL_TEXTURE1);
	glBindTexture(GL_TEXTURE_2D, depthMap);
	glActiveTexture(0);

	debugDepthQuadShader.Use();
	debugDepthQuadShader.SetInt("depthMap", 1);
	debugDepthQuadShader.SetFloat("near_plane", near_plane);
	debugDepthQuadShader.SetFloat("far_plane", far_plane);

	simpleDepthShader.Use();
	simpleDepthShader.SetMat4("lightSpaceMatrix", lightSpaceMatrix);

	shadowBaseShader.Use();
	shadowBaseShader.SetInt("diffuseTexture", 0);
	shadowBaseShader.SetInt("shadowMap", 1);
	shadowBaseShader.SetVec3("lightPos", glm::normalize(lightPos));
	shadowBaseShader.SetMat4("lightSpaceMatrix", lightSpaceMatrix);

	while (!glfwWindowShouldClose(window))
	{
		CommonBaseScript::ProcessInput(window);
		camera.DoKeyboardMove(window);

		//1.渲染阴影的深度图
		glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
		glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
		glClear(GL_DEPTH_BUFFER_BIT);
		simpleDepthShader.Use();
		// simpleDepthShader.SetMat4("lightSpaceMatrixLocation", lightSpaceMatrix);
		RenderScene(simpleDepthShader);
		glBindFramebuffer(GL_FRAMEBUFFER, 0);

		//2.渲染场景的物体
		glViewport(0, 0, SCR_WIDTH, SCR_HEIGHT);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
		shadowBaseShader.Use();
		shadowBaseShader.SetMat4("viewProjection"
		                         , camera.GetProjectionMat4() * camera.GetViewMat4());
		shadowBaseShader.SetVec3("viewPos", camera.position);
		RenderScene(shadowBaseShader);

		//用于debug光的深度图
		/*
		debugDepthQuadShader.Use();
		glBindVertexArray(quadVAO);
		//GL_TRIANGLE_STRIP是将顶点传递给opengl渲染管道线（pipeline）
		//进行进一步处理的方式（创建几何图形）
		glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
		glBindVertexArray(0);
		*/

		glfwSwapBuffers(window);
		glfwPollEvents();
	}

	glDeleteVertexArrays(1, &planeVAO);
	glDeleteVertexArrays(1, &cubeVAO);
	glDeleteVertexArrays(1, &quadVAO);

	glfwTerminate();

	return 0;
}

void _27_ShadowMapping_Base::BindPlaneVAO()
{
	//plane
	//---------------------
	float planeVertices[] =
	{
		// positions            // normals         // texcoords
		25.0f, -0.5f, 25.0f, 0.0f, 1.0f, 0.0f, 25.0f, 0.0f,
		-25.0f, -0.5f, 25.0f, 0.0f, 1.0f, 0.0f, 0.0f, 0.0f,
		-25.0f, -0.5f, -25.0f, 0.0f, 1.0f, 0.0f, 0.0f, 25.0f,

		25.0f, -0.5f, 25.0f, 0.0f, 1.0f, 0.0f, 25.0f, 0.0f,
		-25.0f, -0.5f, -25.0f, 0.0f, 1.0f, 0.0f, 0.0f, 25.0f,
		25.0f, -0.5f, -25.0f, 0.0f, 1.0f, 0.0f, 25.0f, 10.0f
	};
	glGenVertexArrays(1, &planeVAO);
	unsigned int planeVBO;
	glGenBuffers(1, &planeVBO);
	glBindVertexArray(planeVAO);
	glBindBuffer(GL_ARRAY_BUFFER, planeVBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(planeVertices), planeVertices, GL_STATIC_DRAW);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(0));
	glEnableVertexAttribArray(1);
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(3 * sizeof(float)));
	glEnableVertexAttribArray(2);
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), reinterpret_cast<void*>(6 * sizeof(float)));
	glBindBuffer(GL_ARRAY_BUFFER, 0);
	glBindVertexArray(0);
}

